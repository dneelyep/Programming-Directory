<?php //Five different types of literal expressions
$myname = "Daniel";
$myage = "19";
echo "a: " . 73 	 . "<br />"; // A numeric literal
echo "b: " . "Hello" . "<br />"; // A String literal
echo "c: " . TRUE    . "<br />"; // Constant literal (TRUE is a constant, can't be changed in PHP)
echo "d: " . $myname . "<br />"; // Variable string literal
echo "e: " . $myage  . "<br />"; // Variable numeric literal
?>